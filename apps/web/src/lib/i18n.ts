// ─── Types ────────────────────────────────────────────────────────────────────

type TranslationKey = keyof typeof translations.ko;
type TranslationValue = (typeof translations.ko)[TranslationKey];

// ─── Hook ─────────────────────────────────────────────────────────────────────

export function useT() {
  const locale = useLocaleStore((s) => s.locale);
  const dict = translations[locale] as typeof translations.ko;

  function t(key: TranslationKey): string;
  function t(key: TranslationKey, ...args: number[]): string;
  function t(key: TranslationKey, ...args: number[]): string {
    const val = dict[key] ?? (translations.ko[key] as TranslationValue);
    if (typeof val === "function") {
      return (val as (...a: number[]) => string)(...args);
    }
    return val as string;
  }

  return t;
}

export type { TranslationKey };
export { translations };
